#include<iostream>

using namespace std;

int main() {
    // variable de tipo entero
    int entero = 15;
    cout<<entero<<endl;

    // variable de tipo flotante
    float flotante = 10.45;
    cout<<flotante<<endl;

    // variable de tipo double (ocupa más que float)
    double mayor= 16.3456;
    cout<<mayor<<endl;

    // variable de tipo char
    char letra = 'a';
    cout<<letra<<endl;

    return 0;
}
